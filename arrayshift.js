// método que remove o primeiro elemento de um array .shift()
// vai criar uma variavel de matérias
let materias = ["portugues", "matematica", "biologia", "fisica"];
// vai pegar o array e remover o primeiro elemento 
materias.shift();
// vai exibir o array com o primeiro elemento removido
console.log(materias);

//Saida esperada:
//[ 'matematica', 'biologia', 'fisica' ]

// segundo exemplo
let esportes = ["handebol", "volei", "futebol", "basquete"];
esportes.shift();
console.log(esportes);

//Saida esperada:
//[ 'volei', 'futebol', 'basquete' ]
