// método que remove o último elemento de um array .pop()
// vai criar uma variavel de marcas
let marcas = ["Nike", "Adidas", "Gucci", "Prada", "Dior"];
// vai remover o último elemento do array
marcas.pop(marcas);
// vai exibir o array com o último elemento removido
console.log(marcas);

//Saida esperada:
//[ 'Nike', 'Adidas', 'Gucci', 'Prada' ]

// segundo exemplo
let redes = ["Instagram", "Facebook", "Whatsapp", "Spotify", "Gmail"];
redes.pop(redes);
console.log(redes);

//Saida esperada:
//[ 'Instagram', 'Facebook', 'Whatsapp', 'Spotify' ]